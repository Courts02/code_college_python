mood = ['happy', 'sad', 'angry', 'excited', 'tired', 'bored', 'hungry', 'confused', 'anxious', 'stressed']
for mood in mood:
    if 'happy' in mood:
        print("I am happy!")
    elif 'sad' in mood:
        print("I am sad.")
    elif 'angry' in mood:
        print("I am angry.")
    elif 'excited' in mood:
        print("I am excited!")
    elif 'tired' in mood:
        print("I am tired.")
    elif 'bored' in mood:
        print("I am bored.")
    elif 'hungry' in mood:
        print("I am hungry.")
    elif 'confused' in mood:
        print("I am confused.")
    elif 'anxious' in mood:
        print("I am anxious.")
    elif 'stressed' in mood:
        print("I am stressed.")
    else:
        print("I am feeling fine.")